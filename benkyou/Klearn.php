      <p id="paragraf">While katakana (カタカナ) represents the same sounds as hiragana, it’s mainly used to represent foreign words. Foreign names are represented in katakana, as are many foreign foods. Japan’s fun and quirky onomatopoeia appears in both katakana and hiragana. Katakana characters take a boxier form than hiragana characters do, and appear simpler than kanji. Every katakana character has a hiragana counterpart that makes the same sound.

レディー・ガガ (Lady Gaga)
ボン・ジョヴィ (Bon Jovi)
ジョン・スミス (John Smith)

The small circle in between the previous names separates a first name from a last name (or separates two names) so Japanese readers can tell where a foreign name begins and ends.

ビールを飲んでみましょう！(Let’s drink beer!)
私はアメリカンフットボールが好きです！(I like American football*.)
マクドナルドで食べる。(Eat at McDonald’s.)

*American football can also be called アメフト.

Because these words aren’t native to Japan, they’re written in katakana. Many sound words (like sound effects in manga or animal noises) are also written in katakana.</p>
